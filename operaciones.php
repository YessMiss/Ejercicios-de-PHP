    <h1>Ejemplo de operaciones aritméticas en PHP</h1>
    <?php
    $a = 8;
    $b = 3;

    // Operaciones básicas
    echo "Suma: " . ($a + $b) . "<br>"; 
    echo "Resta: " . ($a - $b) . "<br>"; 
    echo "Multiplicación: " . ($a * $b) . "<br>"; 
    echo "División: " . ($a / $b) . "<br>";

    // Operadores de incremento y decremento
    $a++;  // Incremento
    echo "Incremento de a: " . $a . "<br>";

    $b--;  // Decremento
    echo "Decremento de b: " . $b . "<br>";
    ?>
